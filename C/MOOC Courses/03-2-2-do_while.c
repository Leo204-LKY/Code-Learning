// do-while 循环
// 进入循环的时候不做检查，而是在执行完一轮循环体的代码后再来检查循环的条件是否满足，满足则继续进行下一轮循环，不满足则结束循环
// >> do
// {
//     <循环体语句>
// } while (<循环条件>);  // <= 注意最后要加分号

// 对 03-2-1 中的代码进行优化：

#include <stdio.h>

int main()
{
    int x;
    int n = 0;

    scanf("%d", &x);

    do
    {
        n++;
        x /= 10;
    } while (x > 0);
    
    printf("%d", n);
}